async function installPWA() {
  if (!deferredPrompt) {
    showToast(_getToast('pwa_hint'));
    return;
  }
  deferredPrompt.prompt();
  const { outcome } = await deferredPrompt.userChoice;
  if (outcome === 'accepted') showToast(_getToast('pwa_ok'));
  deferredPrompt = null;
}

