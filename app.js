// ==================== NUTRITION CALCULATOR ====================
function calcNutrition() {
  const lang = window._currentLang || 'en';
  const t = T[lang];
  const age = parseFloat(document.getElementById('c_age').value);
  const weight = parseFloat(document.getElementById('c_weight').value);
  const height = parseFloat(document.getElementById('c_height').value);
  const sex = document.getElementById('c_sex').value;
  const activityFactor = parseFloat(document.getElementById('c_activity').value);
  const goalType = document.getElementById('c_goaltype').value;

  if (!age || !weight || !height || age<10 || age>100 || weight<20 || weight>300 || height<100 || height>250) {
    alert(t.err_fill); return;
  }

  // Mifflin–St Jeor BMR
  let bmr;
  if (sex === 'm') {
    bmr = (10 * weight) + (6.25 * height) - (5 * age) + 5;
  } else {
    bmr = (10 * weight) + (6.25 * height) - (5 * age) - 161;
  }

  let tdee = bmr * activityFactor;

  // Goal adjustment
  let goalAdj = 0;
  let goalText = '';
  if (goalType === 'bulk')     { goalAdj = +250; goalText = '+250 kcal'; }
  else if (goalType === 'cut') { goalAdj = -300; goalText = '−300 kcal'; }
  else                         { goalAdj = 0;    goalText = '0 kcal'; }

  const targetKcal = Math.round(tdee + goalAdj);

  // Macros — science-based for muscle gain
  // Protein: 2.0g/kg bodyweight
  const proteinG = Math.round(weight * 2.0);
  const proteinKcal = proteinG * 4;

  // Fat: 25% of total calories
  const fatKcal = Math.round(targetKcal * 0.25);
  const fatG = Math.round(fatKcal / 9);

  // Carbs: remainder
  const carbsKcal = targetKcal - proteinKcal - fatKcal;
  const carbsG = Math.round(Math.max(carbsKcal, 0) / 4);

  // Render results
  document.getElementById('r_tdee').textContent = targetKcal.toLocaleString();
  document.getElementById('r_bmr').textContent = Math.round(bmr).toLocaleString();
  document.getElementById('r_activity_factor').textContent = '×' + activityFactor;
  document.getElementById('r_goal_text').textContent = goalText;

  document.getElementById('r_protein_g').textContent = proteinG;
  document.getElementById('r_protein_kcal').textContent = proteinKcal + ' kcal (' + Math.round(proteinKcal/targetKcal*100) + '%)';
  document.getElementById('r_protein_bar').style.width = Math.round(proteinKcal/targetKcal*100) + '%';

  document.getElementById('r_carbs_g').textContent = carbsG;
  document.getElementById('r_carbs_kcal').textContent = carbsKcal + ' kcal (' + Math.round(carbsKcal/targetKcal*100) + '%)';
  document.getElementById('r_carbs_bar').style.width = Math.round(carbsKcal/targetKcal*100) + '%';

  document.getElementById('r_fat_g').textContent = fatG;
  document.getElementById('r_fat_kcal').textContent = fatKcal + ' kcal (' + Math.round(fatKcal/targetKcal*100) + '%)';
  document.getElementById('r_fat_bar').style.width = '25%';

  const results = document.getElementById('calcResults');
  results.classList.add('visible');
  setTimeout(() => results.scrollIntoView({behavior:'smooth', block:'nearest'}), 100);
}

